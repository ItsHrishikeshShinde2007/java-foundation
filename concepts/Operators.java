public class Operators
{
    public static void main(String[] args)
    {
        System.out.println("Max Verstappen has: ");
        System.out.println( (70 + 1) +  " Career wins");
        System.out.println( (6 - 2) + " world titles");
        System.out.println( (5 * 2) + " Consecutive wins");
        System.out.println( (725 / 5) + " Total career podiums");
        System.out.println( (161 % 71) + " wins in 2023");
    }
}